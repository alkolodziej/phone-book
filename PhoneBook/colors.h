#ifndef COLORS_H
#define COLORS_H

// Reset color
#define COLOR_RESET "\033[0m" // Reset for resetting the color to default.

// Bold
#define COLOR_BOLD "\033[1m" // Bold for menu headers and user input prompts.

// Colors
#define COLOR_RED "\033[31m"    // Red color for error messages
#define COLOR_GREEN "\033[32m"  // Green color for success messages
#define COLOR_YELLOW "\033[33m" // Yellow color for menu options
#define COLOR_BLUE "\033[34m"   // Blue color for informational messages and program end options

#endif /* COLORS_H */